  #Counts to 1-50
print("counting to 1-50 increments of 1")
for a in range(1, 51): #The range starts at 1 and goes up to 50 by 1
    print(a)
  #Counts down to 50-0
print("counting down 50-0 decrements of 1")
for b in range (50, -1, -1): #The range starts at 50 and goes down to 0 by 1
   print(b)
  #Counts to 30-50
print("counting from 30-50 increments of 1")
for c in range(30,51): #The range starts at 30 and goes up to 50 by 1
    print(c)
  #Counts down to 50-10
print("counting down 50-10 decrements of 2")
for d in range(50, 9,-2): #The range starts at 50 and goes down to 10 by -2
 print(d)
